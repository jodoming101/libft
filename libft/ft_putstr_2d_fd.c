#include "libft.h"

void	ft_putstr_2d_fd(char const **s, int fd)
{
	int		i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		ft_putendl_fd(s[i], fd);
		i++;
	}
}
